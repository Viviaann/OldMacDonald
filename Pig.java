class Pig implements Animal 
{     
     private String mySound;      
     public Pig(String type, String sound)     {         
         myType = type;         
         mySound = sound;     
     }     
     public Pig()     {         
         myType = "unknown";         
         mySound = "unknown";     
     }    
     public void setType()
     {myType=type;
     	
     } 
     public void setSound()
     {
     	mySound=sound;
     }
     public getType()
     {
     	return myType;
     }  
     public getSound()
     {
     	return mySound;
     }   
}
